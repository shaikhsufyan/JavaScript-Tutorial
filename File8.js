// Switch Statement
/* The switch statement executes a block of code depending on 
different cases. The switch statement is a part of JavaScript's
 "Conditional" Statements */



 let day = 6;

 switch(day){
    case 0:
        console.log("Monday");
        break;
    case 1:
        console.log("Tuesday");
        break;
    case 2:
        console.log("Wednesday");
        break;
                
    case 3:
        console.log("Thursday");
        break;
    case 4:
        console.log("Friday");
        break;
    case 5:
        console.log("Saturday");
        break;
    case 6:
        console.log("Sunday");
        break;
      
 }




































